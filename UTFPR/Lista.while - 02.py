Marcos = int(52)
feto1 = int(12)
feto2 = int(10)
ano = int(0)
while Marcos != feto1 + feto2:
    Marcos = Marcos + 1
    feto1 = feto1 + 1
    feto2 = feto2 + 1
    ano = ano + 1
print(f'Marcos tera a idade igual a soma da idade de seus filhos em {ano} anos, ele tera {Marcos} anos e seus filhos {feto1} e {feto2}')
